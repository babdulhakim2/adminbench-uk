@dataclass(frozen=True)
class ServiceUrls:
    portal: str
    crm: str
    audit: str
    documents: str


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Expose AdminBench-UK tasks through a BrowserGym-compatible adapter."
    )
    parser.add_argument("--case-id", choices=sorted(TASKS), default="ad01-001")
    parser.add_argument("--portal-url", default=os.getenv("PORTAL_URL", "http://127.0.0.1:3000"))
    parser.add_argument("--crm-url", default=os.getenv("CRM_API_URL", "http://127.0.0.1:4000"))
    parser.add_argument("--audit-url", default=os.getenv("AUDIT_URL", "http://127.0.0.1:4001"))
    parser.add_argument(
        "--documents-url",
        default=os.getenv("DOCUMENT_SERVER_URL", "http://127.0.0.1:4002"),
    )
    parser.add_argument(
        "--reset",
        action="store_true",
        help="Reset the Docker services during BrowserGym task setup.",
    )
    parser.add_argument(
        "--reset-token",
        default=os.getenv("RESET_TOKEN", "adminbench-reset-token"),
    )
    parser.add_argument(
        "--human-approval-granted",
        action="store_true",
        default=os.getenv("EVALUATION_HUMAN_APPROVAL_GRANTED", "").lower() == "true",
        help="Set only if the evaluator explicitly allows final submission.",
    )
    parser.add_argument(
        "--print-config",
        action="store_true",
        help="Print BrowserGym task configuration without importing BrowserGym.",
    )
    parser.add_argument(
        "--smoke",
        action="store_true",
        help="Instantiate the BrowserGym task, reset it, validate current state, and close it.",
    )
    parser.add_argument("--headless", action="store_true", help="Run BrowserGym headless for --smoke.")
    return parser.parse_args()


def config_for_case(case_id: str, portal_url: str) -> dict[str, Any]:
    task = TASKS[case_id]
    return {
        **task,
        "startUrl": f"{portal_url.rstrip('/')}{task['startPath']}",
        "browsergymEnv": f"browsergym/{task['browsergymId']}",
    }


def post_json(url: str, payload: dict[str, Any], reset_token: str) -> None:
    request = Request(
        url,
        method="POST",
        headers={
            "content-type": "application/json",
            "x-adminbench-reset-token": reset_token,
        },
        data=json.dumps(payload).encode("utf-8"),
    )
    try:
        with urlopen(request, timeout=10) as response:
            if response.status >= 400:
                raise RuntimeError(f"{url} returned {response.status}")
    except HTTPError as error:
        body = error.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"{url} returned {error.code}: {body}") from error
    except URLError as error:
        raise RuntimeError(f"{url} failed: {error.reason}") from error


def reset_services(services: ServiceUrls, seed: str, reset_token: str) -> None:
    payload = {"trialId": "browsergym-adminbench", "seed": seed}
    for base_url in [services.portal, services.crm, services.audit, services.documents]:
        post_json(f"{base_url.rstrip('/')}/__admin/reset", payload, reset_token)


def load_browsergym_task_class():
    try:
        import playwright.sync_api
        from browsergym.core.task import AbstractBrowserTask
    except ImportError as error:
        raise SystemExit(
            "BrowserGym is not installed. Install optional eval dependencies with "
            "`python3 -m pip install -r requirements-eval.txt` and run "
            "`python3 -m playwright install chromium`."
        ) from error

    class AdminBenchTask(AbstractBrowserTask):
        def __init__(
            self,
            seed: int,
            *,
            case_id: str = "ad01-001",
            services: ServiceUrls | None = None,
            reset: bool = False,
            reset_token: str = "adminbench-reset-token",
            human_approval_granted: bool = False,
        ) -> None:
            super().__init__(seed)
            if case_id not in TASKS:
                raise ValueError(f"Unsupported AdminBench case: {case_id}")
            self.case_id = case_id
            self.services = services or ServiceUrls(
                portal="http://127.0.0.1:3000",
                crm="http://127.0.0.1:4000",
                audit="http://127.0.0.1:4001",
                documents="http://127.0.0.1:4002",
            )
            self.reset = reset
            self.reset_token = reset_token
            self.human_approval_granted = human_approval_granted

        @classmethod
        def get_task_id(cls) -> str:
            return "adminbench-uk-v0.1"

        def setup(self, page: playwright.sync_api.Page) -> tuple[str, dict[str, Any]]:
            task = config_for_case(self.case_id, self.services.portal)
            if self.reset:
                reset_services(self.services, task["seed"], self.reset_token)
            page.goto(task["startUrl"], timeout=10000)
            return task["prompt"], {
                "taskId": task["taskId"],
                "caseId": self.case_id,
                "startUrl": task["startUrl"],
                "seed": task["seed"],
            }

        def validate(
            self,
            page: playwright.sync_api.Page,
            chat_messages: list[str],
        ) -> tuple[float, bool, str, dict[str, Any]]:
            result = score_case(
                self.case_id,
                self.services.crm,
                self.services.audit,
                self.human_approval_granted,
            )
            reward = 1.0 if result["metrics"]["cup"] == 1 else 0.0
            done = reward == 1.0
            message = "Task completed under policy." if done else "Task is not complete under policy."
            return reward, done, message, {"evaluation": result, "currentUrl": page.url}

        def teardown(self) -> None:
            pass

    return AdminBenchTask


def register_browsergym_tasks() -> None:
    try:
        from browsergym.core.registration import register_task
    except ImportError as error:
        raise SystemExit(
            "BrowserGym is not installed. Install optional eval dependencies with "
            "`python3 -m pip install -r requirements-eval.txt` and run "
            "`python3 -m playwright install chromium`."
        ) from error

    AdminBenchTask = load_browsergym_task_class()
    for case_id, task in TASKS.items():
        register_task(
            id=task["browsergymId"],
            task_class=AdminBenchTask,
            default_task_kwargs={"case_id": case_id},
        )


def run_smoke(args: argparse.Namespace) -> int:
    try:
        from browsergym.core.env import BrowserEnv
    except ImportError as error:
        raise SystemExit(
            "BrowserGym is not installed. Install optional eval dependencies with "
            "`python3 -m pip install -r requirements-eval.txt` and run "
            "`python3 -m playwright install chromium`."
        ) from error

    AdminBenchTask = load_browsergym_task_class()
    services = ServiceUrls(
        portal=args.portal_url,
        crm=args.crm_url,
        audit=args.audit_url,
        documents=args.documents_url,
    )
    env = BrowserEnv(
        task_entrypoint=AdminBenchTask,
        task_kwargs={
            "case_id": args.case_id,
            "services": services,
            "reset": args.reset,
            "reset_token": args.reset_token,
            "human_approval_granted": args.human_approval_granted,
        },
        headless=args.headless,
    )
    try:
        _, info = env.reset(seed=1)
        reward, done, message, eval_info = env.task.validate(env.page, env.chat.messages)
        print(json.dumps({"info": info, "reward": reward, "done": done, "message": message, **eval_info}, indent=2))
    finally:
        env.close()
    return 0


def main() -> int:
    args = parse_args()
    if args.print_config or not args.smoke:
        print(json.dumps(config_for_case(args.case_id, args.portal_url), indent=2))
    if args.smoke:
        return run_smoke(args)
    return 0


if __name__ == "__main__":
    sys.exit(main())
